#include "libft.h"

int	ft_isalnum(int c)
{
	if (!((c >= 48 && c <= '9')
			|| (c >= 'a' && c <= 'z')
			|| (c >= 'A' && c <= 'Z' )))
		return (0);
	return (1);
}

